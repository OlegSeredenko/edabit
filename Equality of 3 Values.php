/*На вход подаются три чиссла, необходимо узнать сколько чисел равны друг другу
*/
<?php
function equal($a, $b, $c) {
	if ($a == $b && $b == $c) {
		return 3;
	} elseif (($a == $b && $b != $c) || ($a != $b && $b == $c) || ($a != $c && $b == $a) || ($a == $c && $b != $a)) {
		return 2;
	} else {
		return 0;
	}
}
?>