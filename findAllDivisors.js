/*на вход подается 7 чисел от пользователя, неободимо узнать сколько чисел больше предыдущего числа
*/

let sum = 0;
let num = prompt('Введите число ');
num = Number(num);
for (let i = 1; i <= num; i++) {

    if (num % i == 0) {
        sum = sum + i;
    }
}
console.log( 'для ' + num + ' сумма делителей равна ' + sum);
