/*Дана строка'слова слова слова.'. В конце этой строки может быть точка, аможет и не быть. 
Сделайте так, чтобы в конце этой строки гарантировано стоялаточка. То есть: если этой точки нет –ее надо добавить, а если есть –ничего не делать
*/
<?php
function pointEnd($str)
{
    $str = rtrim($str,  '.');
    return $str . '.';
}
?>