/*На вход подаётся строка. Необхоимо вывести массив с двумя элементами: первый элемент это строка в отсортированном виде по алфавиту, 
второй это то строка в отсортированном виде по алфавиту, нов  обратном порядке. Например для строки "marmite" функция вернёт ["aeimmrt", "trmmiea"]
*/ 
<?php
function firstAndLast($s) {
	$s = str_split($s);
	sort($s);
	$x1 = implode($s);
	$x2 = strrev($x1);
	return [$x1, $x2];
}
?>