/*на вход подается число (допустим x), необходимо получить сумму всех четных чисел от нуля до этого числа (не считая само число)
*/


  
function findSumEvenNumbers(n)
{   
    let sum = 0;
    for (let i = 1; i < n; i++) {
        if (i % 2 == 0) {
            sum = sum + i;
        }
    }
    return " Сумма четных чисел от нуля до " + n  + " равна " + sum;
}
