/*на вход подается 7 чисел от пользователя, неободимо узнать сколько чисел больше предыдущего числа
*/

let count = 0;
let num = prompt('Введите число двухзначное число ');
num = Number(num);
for (let i = 10; i <= 99; i++) {
    if (num % i == 0) {
        count = count + 1;
    }
}
console.log( 'Для ' + num + ' количество двухзначных делителей ' + count);
