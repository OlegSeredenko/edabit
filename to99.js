/*сделать программу, которая принимает с клавиатуры числа (все числа складывает в массив), пока не будет введено 99
*/
let num = 1;
let arr = [];
let sum = 0;
while (num != 99) {
    num = prompt('Введите число ');
    num = Number(num);
    sum = sum + num;
    arr.push(num);
}
let l =  arr.length - 1;
console.log("Получено " + l + " значений" + "\n" + "Полученный массив " + arr + "\n" +" Сумма элементов в массиве " + sum);