/*На вход подаются два числа (число, над которым будет идим перемножение и длина нового массива). Необходимо вывести массив как в примере: если на вход поданы числа 7 и 5, то функция вернёт массив ➞ [7, 14, 21, 28, 35]
*/
<?php
function arrayOfMultiples($num, $length) {
	$new_arr = [];
	$x = $num;
	for ($i = 0; $i < $length; $i++) {
		$new_arr[] = $x;
		$x = $x + $num;
	}
	return $new_arr;
}
?>