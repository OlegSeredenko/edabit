/*сделать программу, которая принимает с клавиатуры два числа (первое меньше второго), и для каждого числа выводит все его делители
*/

function allDivisors(a, b) {
    for (let i = a; i <= b; i++) {
        for (let j = 1; j <= i; j++) {
            if (i  % j == 0) {
                console.log('Делитель числа ' + i + ' это ' + j);
            }
        }
    }
}
