/*Сделайте функцию, которая будет проверять число на совершенность. Совершенное число - это число, сумма собственных делителей которого равна этому числу
Первые совершенные числа в математике это 6, 28, 496, 8128 
*/ 
<?php
function perfectionNumber($a)
{
    $new_arr = [];
    for($i = 1; $i < $a; $i++) {
        if ($a % $i == 0) {
            $new_arr[] = $i;
        }
    }
    return (array_sum($new_arr) == $a) ? true : false;
}
?>