/*На вход подаётся массив с числами, необходимо вывести отсортированный массив
Например для массива [80, 29, 4, -95, -24, 85] функция венрнёт [-95, -24, 4, 29, 80, 85]
*/ 
<?php
function sortNumsAscending($arr) {
	if ($arr == null || empty($arr)) {
		return [];
	} else {
		sort($arr);
		return $arr;
	}
}
?>