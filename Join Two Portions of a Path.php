/*На вход подаётся две строки, возможно в строках присутствует символ '/'. Необходимо вывести строки так, чтоб ыбыл только один символ '/' между строк. Например для строк
"portion1/" и "/portion2" функция вернёт "portion1/portion2"
*/ 
<?php
function joinPath($x, $y) {
	$x = str_replace('/', '',$x);
	$y = str_replace('/', '',$y);
	return $x  . '/' . $y;
}
?>