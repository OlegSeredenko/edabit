/*Переменная n хранит целое число от 0 до 9. Используя оператор switch, написать скрипт, 
который в зависимости от числа будет выводить слово (Например, если n равно 3, то будет выводиться слово «три»)кратные 10
*/
let n = 1;
switch (n) {
  case 1:
    alert( 'один' );
    break;
  case 2:
    alert( 'два' );
    break;
  case 3:
    alert( 'три' );
    break;
  case 4:
    alert( 'четыре' );
    break;
  case 5:
    alert( 'пять' );
    break;
  case 6:
    alert( 'шесть' );
    break;
  case 7:
    alert( 'семь' );
    break;
  case 8:
    alert( 'восемь' );
    break;
  case 9:
    alert( 'девять' );
    break;
  case 0:
    alert( 'ноль' );
    break;
}