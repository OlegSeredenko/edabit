/*сделать программу, которая принимает с клавиатуры число и проверяет, можно ли представить это число в виде суммы квадартов однозначных чисел
*/

function allDivisors(a) {

    for (let i = 1; i <= 9; i++) {
        let x = i ** 2;
        for (let j = 0; j <= 9; j++) {
            let y = j ** 2;
            if ((x + y) == a) {
                console.log("число " + a + " можно представить в виде суммы квадратов " + i + " и " + j);
            }
        }
    }
}

